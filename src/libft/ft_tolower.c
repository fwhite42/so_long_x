static inline int	ft_isuppercase(char c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);
	return (0);
}

int	ft_tolower(int c)
{
	if (ft_isuppercase(c))
		c += 'a' - 'A';
	return (c);
}
